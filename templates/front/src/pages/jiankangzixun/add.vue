                :style='{"minHeight":"200px","padding":"0","boxShadow":"0 0 6px #eee","margin":"0","borderColor":"#ccc","backgroundColor":"#F6F6F6","borderRadius":"0","borderWidth":"0","width":"100%","borderStyle":"solid","height":"auto"}'
